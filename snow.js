class Player {
    newPlayer() {
        this.life = 5;
        this.action = "‚ùÑÔ∏è";
        this.accion = "‚ùÑÔ∏è";
        this.taunt = 0;
    }

    getVida() {
        return this.life;
    }

    getAction() {
        return this.action;
    }

    getAccion() {
        return this.accion;
    }

    setAction(act) {
        this.action = act;
    }

    setAccion(acc) {
        this.accion = acc;
    }

    damage(dmg) {
        this.life -= dmg;
    }

    setTaunt() {
        this.taunt++;
    }

    getTaunt() {
        return this.taunt;
    }
}

